import { Router } from 'express';
import { query, queryOne, execute } from '../../db/index.js';
import { AuthRequest, requireAuth, requireAdmin } from '../../middleware/auth.js';
import { v4 as uuidv4 } from 'uuid';

const router = Router();
router.use(requireAuth());
router.use(requireAdmin());

/**
 * @swagger
 * /api/admin/teams:
 *   get:
 *     summary: Get all teams
 *     tags: [Admin - Teams]
 *     security:
 *       - cookieAuth: []
 *       - bearerAuth: []
 *     responses:
 *       200:
 *         description: List of teams
 */
router.get('/', async (req, res) => {
  const authReq = req as AuthRequest;
  try {
    const teams = await query('SELECT * FROM teams ORDER BY created_at DESC', []);
    const teamsList = Array.isArray(teams) ? teams : (teams ? [teams] : []);
    res.json(teamsList);
  } catch (error: any) {
    res.status(500).json({ error: error.message });
  }
});

/**
 * @swagger
 * /api/admin/teams/{id}:
 *   get:
 *     summary: Get team by ID with members
 *     tags: [Admin - Teams]
 *     security:
 *       - cookieAuth: []
 *       - bearerAuth: []
 *     parameters:
 *       - in: path
 *         name: id
 *         required: true
 *         schema:
 *           type: string
 *     responses:
 *       200:
 *         description: Team with members
 */
router.get('/:id', async (req, res) => {
  const authReq = req as AuthRequest;
  try {
    const { id } = req.params;
    const team = await queryOne('SELECT * FROM teams WHERE id = ?', [id]);
    if (!team) {
      return res.status(404).json({ error: 'Team not found' });
    }

    const members = await query(
      `SELECT u.id, u.email, u.name, u.is_admin 
       FROM users u 
       INNER JOIN team_members tm ON u.id = tm.user_id 
       WHERE tm.team_id = ?`,
      [id]
    );

    res.json({
      ...team,
      members: Array.isArray(members) ? members : (members ? [members] : []),
    });
  } catch (error: any) {
    res.status(500).json({ error: error.message });
  }
});

/**
 * @swagger
 * /api/admin/teams:
 *   post:
 *     summary: Create a new team
 *     tags: [Admin - Teams]
 *     security:
 *       - cookieAuth: []
 *       - bearerAuth: []
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             required:
 *               - name
 *             properties:
 *               name:
 *                 type: string
 *               description:
 *                 type: string
 *     responses:
 *       201:
 *         description: Team created
 */
router.post('/', async (req, res) => {
  const authReq = req as AuthRequest;
  try {
    const { name, description } = req.body;

    if (!name) {
      return res.status(400).json({ error: 'Name is required' });
    }

    const existing = await queryOne('SELECT id FROM teams WHERE name = ?', [name]);
    if (existing) {
      return res.status(400).json({ error: 'Team with this name already exists' });
    }

    const teamId = uuidv4();
    await execute(
      'INSERT INTO teams (id, name, description) VALUES (?, ?, ?)',
      [teamId, name, description || null]
    );

    const team = await queryOne('SELECT * FROM teams WHERE id = ?', [teamId]);
    res.status(201).json(team);
  } catch (error: any) {
    if (error.message && (error.message.includes('UNIQUE constraint') || error.message.includes('duplicate'))) {
      return res.status(400).json({ error: 'Team with this name already exists' });
    }
    res.status(500).json({ error: error.message });
  }
});

/**
 * @swagger
 * /api/admin/teams/{id}:
 *   put:
 *     summary: Update team
 *     tags: [Admin - Teams]
 *     security:
 *       - cookieAuth: []
 *       - bearerAuth: []
 *     parameters:
 *       - in: path
 *         name: id
 *         required: true
 *         schema:
 *           type: string
 *     requestBody:
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             properties:
 *               name:
 *                 type: string
 *               description:
 *                 type: string
 *     responses:
 *       200:
 *         description: Team updated
 */
router.put('/:id', async (req, res) => {
  const authReq = req as AuthRequest;
  try {
    const { id } = req.params;
    const { name, description } = req.body;

    const existing = await queryOne('SELECT * FROM teams WHERE id = ?', [id]);
    if (!existing) {
      return res.status(404).json({ error: 'Team not found' });
    }

    if (name && name !== (existing as any).name) {
      const nameExists = await queryOne('SELECT id FROM teams WHERE name = ? AND id != ?', [name, id]);
      if (nameExists) {
        return res.status(400).json({ error: 'Team with this name already exists' });
      }
    }

    const updates: string[] = [];
    const params: any[] = [];

    if (name !== undefined) {
      updates.push('name = ?');
      params.push(name);
    }
    if (description !== undefined) {
      updates.push('description = ?');
      params.push(description);
    }

    if (updates.length === 0) {
      return res.status(400).json({ error: 'No fields to update' });
    }

    params.push(id);
    await execute(`UPDATE teams SET ${updates.join(', ')} WHERE id = ?`, params);

    const team = await queryOne('SELECT * FROM teams WHERE id = ?', [id]);
    res.json(team);
  } catch (error: any) {
    res.status(500).json({ error: error.message });
  }
});

